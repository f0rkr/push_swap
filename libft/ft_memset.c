#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	int				*str;
	size_t			i;

	i = 0;
	str = (int *)b;
	while (i < len)
	{
		str[i] = c;
		i++;
	}
	return (b);
}
